// ========================================= React.js ka Introduction ============================================ //
// ChatGPT Se Liya Hua Syntex ---> React.js Kya Hai.
// React.js ek JavaScript library hai jo user interfaces (UI) banane ke liye use hoti hai, specifically single-page applications ke liye. Iska primary focus hai fast aur interactive user interfaces create karna. React.js component-based architecture ko follow karta hai, jisme UI ko chhote, reusable components mein tod diya jata hai. Yeh components independently manage aur update hote hain, jo ki development ko efficient banata hai.


// ========================================== React ki Key Features ============================================= //
// ChatGPT Se Liya Hua Syntex ---> React ki Key Features.
// 1. Component-Based Architecture: UI ko chhote, independent components me tod sakte hain, jo easily manage aur reuse ho sakte hain.

// 2. Virtual DOM: Real DOM ke bajaye, React ek Virtual DOM use karta hai jo changes ko efficiently handle karta hai aur performance ko badhata hai.

// 3. Declarative Syntax: React me hum declarative syntax use karte hain, jisme hum define karte hain ki UI kaisa dikhega, aur React DOM ko update karne ki responsibility apne aap le leta hai.

// 4. JSX: JavaScript XML ya JSX ek syntax extension hai jo JavaScript me HTML likhne ko allow karta hai. Isse code readable aur maintainable hota hai.

// 5. Unidirectional Data Flow: Data React components me ek hi direction me flow karta hai, jo debugging aur maintenance ko aasaan banata hai.


// =========================================== React.js ka History =============================================== //
// ChatGPT Se Liya Hua Syntex ---> React.js ki History.

// 1. 2011: Facebook ne React ka development start kiya. Us time pe Facebook ke developer, Jordan Walke, ne ek JavaScript library banayi jo DOM ko fast aur efficiently update kar sake.

// 2. 2013: React ko officially release kiya gaya aur open-source banaya gaya. Initial release ka naam "React.js" tha.

// 3. 2015: React Native ko launch kiya gaya, jo mobile app development ke liye use hota hai. React Native ke saath, hum JavaScript me mobile apps bana sakte hain jo native feel aur performance dete hain.

// 4. 2017: Facebook ne React v16 release kiya, jisme Fiber Reconciliation Engine introduce kiya gaya. Ye React ke performance aur flexibility ko badhata hai.

// 5. 2020: React v17 release hua, jisme mainly internal changes kiye gaye without introducing new features. Iska focus backward compatibility aur easy upgrade pe tha.




// =========================================== React  Major Updates ============================================== //
// ChatGPT Se Liya Hua Syntex ---> React.js Ke Major Updates.

// 1. React 0.14 (October 2015)
// Update: React aur React DOM ko alag-alag packages me split kiya gaya.
// Key Benefit: Is se code sharing aur modularity better hui.

// 2. React 15 (April 2016)
// Update: Bug fixes aur performance improvements kiye gaye.
// Key Benefit: Is se application ki reliability aur floating-point number rendering better hui.

// 3. React 16 ("Fiber") (September 2017)
// Update: Fiber architecture ko introduce kiya gaya.
// Key Features: Error Boundaries, Portals, Improved SSR.
// Key Benefit: Is se performance aur flexibility badhi.


// 4. React 16.8 ("Hooks") (February 2019)
// Update: Hooks ko introduce kiya gaya.
// Key Hooks: useState, useEffect, useContext.
// Key Benefit: Functional components ke saath development simple aur flexible ban gayi.


// 5. React 17 (October 2020)
// Update: Yeh "No new features" release thi, jo backward compatibility par focus karti thi.
// Key Focus: Gradual upgrades aur improved event delegation.
// Key Benefit: Ecosystem ko stable aur resilient banaya.


// 6. React 18 (March 2022)
// Update: Concurrent Rendering ko introduce kiya gaya.
// Key Features: Concurrent Mode, Automatic Batching, New Suspense Features.
// Key Benefit: Applications ko aur responsive aur performant banaya.