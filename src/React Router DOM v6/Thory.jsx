// ======================================= React Router ke Key Features ========================================== //
// ChatGPT Se Liya Hua Syntex ---> React Router ke Key Features.

// 1. Dynamic Routing: Aap runtime par routes ko define kar sakte hain, jisse aapki app ki routing flexible hoti hai.

// 2. Nested Routes: Aap nested routes ka istemal karke complex layouts bana sakte hain. Ye aapko ek route ke andar doosre routes ko define karne ki suvidha deta hai.

// 3. Route Parameters: Aap routes me parameters use kar sakte hain, jisse aap dynamic data fetch kar sakte hain. Jaise, agar aapko kisi user ka profile dikhana hai, toh aap user ID ko parameter ke roop me pass kar sakte hain.

// 4. Redirects and Navigation: Aap easily redirect kar sakte hain kisi route se doosre route par, aur programmatically navigation bhi kar sakte hain.

// 5. Protected Routes: Aap routes ko protect karne ka feature bhi use kar sakte hain, jisse sirf authorized users hi kuch specific routes tak access kar sake.
