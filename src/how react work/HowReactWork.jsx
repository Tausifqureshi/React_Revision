// ==================================================== JSX ====================================================== //
// ChatGPT Se Liya Hua Syntex ---> 2. JSX.
// React JSX (JavaScript XML) ka use karta hai jo ek syntax extension hai JavaScript ke liye. JSX HTML-like syntax ko JavaScript ke code ke saath combine karta hai. Ye React components ko define karne ke liye use hota hai aur React elements ko create karne mein madad karta hai.

// =============================================== Virtual DOM =================================================== //
// ChatGPT Se Liya Hua Syntex ---> 3. Virtual DOM.

// React ek Virtual DOM use karta hai jo ek lightweight copy of the actual DOM hota hai. Jab bhi component ka state ya props change hota hai, React pehle Virtual DOM ko update karta hai aur phir actual DOM ko efficiently update karta hai sirf necessary changes ke saath. Isse performance improve hoti hai.

// =============================================== Virtual DOM =================================================== //
